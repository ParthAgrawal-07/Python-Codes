phrase="Giraffe Academy"
print(phrase.lower() + " is cool") #lower() converts the string to lowercase
print(phrase.upper() + " is cool") #upper() converts the string to uppercase
print(phrase.isupper()) #isupper() checks if the string is in uppercase
print(phrase[0]) #prints the character at index 0
print(phrase.index("G")) #prints the index of the character "G"
print(phrase.replace("Giraffe", "Elephant")) #replaces the word "Giraffe" with "Elephant"